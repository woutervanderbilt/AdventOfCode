        public override Task ExecuteAsync()
        {
            var grid = new Grid<char>();

            (int, int) start = (0, 0);
            (int, int) target = (0, 0);

            int y = 0;
            foreach (var line in input.Split(Environment.NewLine))
            {
                int x = 0;
                foreach (var c in line)
                {
                    grid[x, y] = c;
                    if (c == 'S')
                    {
                        start = (x, y);
                        grid[x, y] = 'a';
                    }

                    if (c == 'E')
                    {
                        target = (x, y);
                        grid[x, y] = 'z';
                    }
                    x++;
                }

                y++;
            }

            HashSet<(int, int)> visited = new HashSet<(int, int)>();
            IList<(int, int)> current = new List<(int, int)>();
            IList<string> paths = new List<string>();
            current.Add(target);
            visited.Add(target);
            paths.Add("S");
            int steps = 0;
            while (true)
            {
                steps++;
                IList<(int, int)> newCurrent = new List<(int, int)>();
                IList<string> newPaths = new List<string>();
                int index = 0;
                foreach (var location in current)
                {
                    var path = paths[index];
                    
                    var height = grid[location.Item1, location.Item2];
                    foreach (var neighbour in grid.Neighbours(location, false))
                    {
                        if ((int)neighbour.value - (int)height >= -1)
                        {
                            if(!visited.Contains((neighbour.x, neighbour.y)))
                            {
                                visited.Add((neighbour.x, neighbour.y));
                                newCurrent.Add((neighbour.x, neighbour.y));
                                newPaths.Add(path+neighbour.value);
                                if (neighbour.value == 'a')
                                {
                                    Result = steps.ToString();
                                    return Task.CompletedTask;
                                }
                            }
                        }
                    }

                    index++;
                }
                current = newCurrent;
                paths = newPaths;
            }

            return Task.CompletedTask;
        }

